package lab01.bai3;

import java.util.Scanner;

public class Bai3 {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Nhập số nguyên n, số thực m: ");
        int n = sc.nextInt();
        float m = sc.nextFloat();
        System.out.println("Số nguyên n vừa nhập: " +n);
        System.out.println("Số thực m vừa nhập: " +m);
    }
}
